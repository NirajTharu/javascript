// Used to perform to operations on data

// Arithmetic operators: +, -, *, /, %, **(a^b),  ++, --

let number1 = 5;
let number2 = 8;

console.log("a =", number1 & "b =", number2);
console.log("a+b", number1 + number2);
console.log("a-b", number1 - number2);
console.log("a*b", number1 * number2);
console.log("a/b", number1 / number2);
console.log("a%b", number1 % number2);
console.log("a**b", number1 ** number2);

number1++;
number2--;
console.log(number1++);
console.log(number2--);

// Assignment operators: =, +=, -=, %=, **=
// number1 = number1+5;
number1 += 5;
console.log(number1);

number1 -= 6;
console.log(number1);

// Comparison operators: returns boolean value(true/false)
// Equal to ==  (only check the value)
// Equal to & type === (will check the value and data type)
// Not equal to !=
// Not equal to & type !==

console.log(number1);
console.log(number2);
console.log(number1 <= number2);
console.log(number1 !== number2);

